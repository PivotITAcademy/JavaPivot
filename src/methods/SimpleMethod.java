package methods;

public class SimpleMethod {

	//Class level variables
	int num1;
	int num2;
	
	
	
	
	/*
	 * ReturnType = void
	 * methodName = addTwoNumbers
	 * () - Empty bracket
	 * { } - Method body 
	 * Method with no arguments and no return
	 * returnType methodName(){ }
	 */
	void addTwoNumbers() {
		
		System.out.println(" Sum of two numbers is "+(num1+num2));
	}
	
	
	
	
}
